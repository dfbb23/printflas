#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;

	if (!s )
		return (write(1,"(null)",6));
	i = 0;
	while (s[i])
	{
		ft_putchar(s[i]);
		i++;
	}
	return (i);
}
